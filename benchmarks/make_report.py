
"""Run the core benchmark suite and generate polished reports.

The script creates machine-readable CSV files, PNG plots, a Markdown report, and a
standalone HTML report suitable for sharing or linking from Sphinx documentation.

Example:
    python benchmarks/make_report.py --outdir results/report
"""
from __future__ import annotations

import argparse
import csv
import html
import os
import subprocess
import sys
from pathlib import Path


def run(cmd, cwd, env=None):
    print("$", " ".join(cmd))
    subprocess.run(cmd, cwd=cwd, check=True, env=env)


def read_csv(path):
    with open(path, newline="") as f:
        return list(csv.DictReader(f))


def markdown_table(rows, columns, max_rows=None):
    rows = rows[:max_rows] if max_rows is not None else rows
    if not rows:
        return "_No rows._\n"
    out = []
    out.append("| " + " | ".join(columns) + " |")
    out.append("| " + " | ".join(["---"] * len(columns)) + " |")
    for row in rows:
        out.append("| " + " | ".join(str(row.get(c, "")) for c in columns) + " |")
    return "\n".join(out) + "\n"


def html_table(rows, columns, max_rows=None, highlight_first=True):
    rows = rows[:max_rows] if max_rows is not None else rows
    if not rows:
        return '<p class="muted">No rows.</p>'
    head = ''.join(f'<th>{html.escape(c.replace("_", " ").title())}</th>' for c in columns)
    body = []
    for i, row in enumerate(rows):
        cls = ' class="top"' if highlight_first and i == 0 else ''
        cells = ''.join(f'<td>{html.escape(str(row.get(c, "")))}</td>' for c in columns)
        body.append(f'<tr{cls}>{cells}</tr>')
    return f'<div class="table-wrap"><table><thead><tr>{head}</tr></thead><tbody>{"".join(body)}</tbody></table></div>'


def write_markdown_report(outdir, summary_rows, speed_rows, hard_rows, openmp_rows, anomaly_rows):
    report = []
    report.append("# robustcov benchmark report\n")
    report.append("This report was generated by `benchmarks/make_report.py`. It is intended as a reproducible development report, not a final publication benchmark.\n")

    report.append("## Small-sample heavy-tail summary\n")
    report.append("Lower `mean_rank`, `median_error`, and `mean_error` are better.\n")
    report.append(markdown_table(summary_rows, ["method", "win_rate", "mean_rank", "median_error", "mean_error", "median_seconds"]))
    if (outdir / "small_sample_rank.png").exists():
        report.append("![Small-sample mean-rank summary](small_sample_rank.png)\n")

    if speed_rows:
        report.append("## Speed snapshot\n")
        report.append(markdown_table(speed_rows, ["method", "median_seconds", "min_seconds", "max_seconds"]))
        if (outdir / "speed.png").exists():
            report.append("![Speed comparison](speed.png)\n")

    if openmp_rows:
        report.append("## OpenMP scaling\n")
        report.append("This benchmark uses `OPENBLAS_NUM_THREADS=1` and `MKL_NUM_THREADS=1` to avoid BLAS/OpenMP oversubscription. Higher speedup is better.\n")
        report.append(markdown_table(openmp_rows, ["method", "threads", "median_seconds", "speedup_vs_1"]))
        if (outdir / "openmp_scaling.png").exists():
            report.append("![OpenMP scaling](openmp_scaling.png)\n")

    if anomaly_rows:
        report.append("## Anomaly detection baselines\n")
        report.append("This benchmark compares robustcov detectors with common sklearn anomaly baselines on a reproducible heavy-tailed synthetic tabular task. Higher precision, recall, F1, and ROC AUC are better.\n")
        report.append(markdown_table(anomaly_rows, ["method", "precision", "recall", "f1", "roc_auc", "seconds", "detected"]))
        if (outdir / "anomaly_baselines.png").exists():
            report.append("![Anomaly baseline comparison](anomaly_baselines.png)\n")

    if hard_rows:
        report.append("## Hard contamination scenarios\n")
        report.append("Selected rows from the synthetic hard-scenario benchmark.\n")
        report.append(markdown_table(hard_rows, ["scenario", "method", "rel_fro_error", "seconds", "support_purity", "outlier_leakage", "radial_kurtosis"], max_rows=30))

    report.append("## Interpretation\n")
    report.append("- `RegularizedCauchy` and `StudentTScatter` are the main candidates for small-sample, high-dimensional, heavy-tailed data.\n")
    report.append("- `FastMCD` is best interpreted as the efficient classical contamination estimator when outliers are separable and `n` is comfortably larger than `p`.\n")
    report.append("- `RegularizedTyler` / `KLRegularizedTyler` are useful shape estimators but are not always the best covariance recovery estimators in the heaviest-tail benchmarks.\n")
    report.append("- `HellingerRegularizedTyler` remains experimental until the exact objective/update is finalized.\n")
    report.append("- MVE is intentionally not prioritized: current evidence points to regularized heavy-tail scatter estimators as the stronger differentiator.\n")

    (outdir / "benchmark_report.md").write_text("\n".join(report))


def _card(title, value, subtitle):
    return f'<div class="metric"><div class="metric-title">{html.escape(title)}</div><div class="metric-value">{html.escape(value)}</div><div class="metric-subtitle">{html.escape(subtitle)}</div></div>'


