def write_report(outdir, args):
    summary_rows = read_csv(outdir / "small_sample_summary.csv")
    speed_rows = read_csv(outdir / "speed.csv") if (outdir / "speed.csv").exists() else []
    hard_rows = read_csv(outdir / "hard_scenarios.csv") if (outdir / "hard_scenarios.csv").exists() else []
    openmp_rows = read_csv(outdir / "openmp_scaling.csv") if (outdir / "openmp_scaling.csv").exists() else []
    anomaly_rows = read_csv(outdir / "anomaly_baselines.csv") if (outdir / "anomaly_baselines.csv").exists() else []
    write_markdown_report(outdir, summary_rows, speed_rows, hard_rows, openmp_rows, anomaly_rows)
    write_html_report(outdir, summary_rows, speed_rows, hard_rows, openmp_rows, anomaly_rows)


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--outdir", default="results/report")
    parser.add_argument("--skip-hard", action="store_true")
    parser.add_argument("--skip-speed", action="store_true")
    parser.add_argument("--skip-openmp", action="store_true")
    parser.add_argument("--skip-anomaly", action="store_true")
    parser.add_argument("--openmp-n", type=int, default=8000)
    parser.add_argument("--openmp-p", type=int, default=20)
    parser.add_argument("--openmp-threads", nargs="+", default=["1", "2", "4"])
    args = parser.parse_args()

    root = Path(__file__).resolve().parents[1]
    outdir = (root / args.outdir).resolve() if not Path(args.outdir).is_absolute() else Path(args.outdir)
    outdir.mkdir(parents=True, exist_ok=True)

    py = sys.executable
    run([py, "benchmarks/small_sample_heavy_tail.py", "--csv", str(outdir / "small_sample.csv")], cwd=root)
    run([py, "benchmarks/benchmark_summary.py", "--input", str(outdir / "small_sample.csv"), "--csv", str(outdir / "small_sample_summary.csv"), "--html", str(outdir / "small_sample_summary.html"), "--markdown", str(outdir / "small_sample_summary.md")], cwd=root)
    run([py, "examples/plot_benchmark_summary.py", "--input", str(outdir / "small_sample_summary.csv"), "--metric", "mean_rank", "--output", str(outdir / "small_sample_rank.png")], cwd=root)

    if not args.skip_speed:
        run([py, "benchmarks/speed_estimators.py", "--n", "2000", "--p", "10", "--repeat", "5", "--quality", "fast", "--csv", str(outdir / "speed.csv")], cwd=root)
        run([py, "examples/plot_speed_comparison.py", "--input", str(outdir / "speed.csv"), "--output", str(outdir / "speed.png")], cwd=root)

    if not args.skip_openmp:
        env = os.environ.copy()
        env["OPENBLAS_NUM_THREADS"] = "1"
        env["MKL_NUM_THREADS"] = "1"
        env["OMP_NUM_THREADS"] = str(max(int(t) for t in args.openmp_threads))
        run([py, "benchmarks/openmp_scaling.py", "--n", str(args.openmp_n), "--p", str(args.openmp_p), "--threads", *map(str, args.openmp_threads), "--csv", str(outdir / "openmp_scaling.csv")], cwd=root, env=env)
        run([py, "examples/plot_openmp_scaling.py", "--input", str(outdir / "openmp_scaling.csv"), "--output", str(outdir / "openmp_scaling.png")], cwd=root)

    if not args.skip_anomaly:
        run([py, "benchmarks/anomaly_detection_baselines.py", "--n", "900", "--p", "12", "--contamination", "0.08", "--csv", str(outdir / "anomaly_baselines.csv")], cwd=root)
        run([py, "examples/plot_anomaly_baselines.py", "--input", str(outdir / "anomaly_baselines.csv"), "--metric", "f1", "--output", str(outdir / "anomaly_baselines.png")], cwd=root)

    if not args.skip_hard:
        run([py, "benchmarks/hard_contamination_scenarios.py", "--n", "1000", "--p", "8", "--contamination", "0.2", "--csv", str(outdir / "hard_scenarios.csv")], cwd=root)

    write_report(outdir, args)
    print("wrote", outdir / "benchmark_report.md")
    print("wrote", outdir / "benchmark_report.html")
